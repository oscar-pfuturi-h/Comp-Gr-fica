using namespace std;

#define numVAOs 1
#define numVBOs 2

float cameraX, cameraY, cameraZ;
float cubeLocX, cubeLocY, cubeLocZ;
GLuint renderingProgram;
GLuint vao[numVAOs];
GLuint vbo[numVBOs];
// allocate variables used in display() function, so that they wonâ€™t need to be allocated during rendering
GLuint mvLoc, projLoc, tfLoc, vLoc;
int width, height, i;
float aspect, tf, timeFactor;
glm::mat4 pMat, vMat, mMat, mvMat, tMat, rMat;

const GLchar* vspath = "../vertShader2.glsl";
const GLchar* fspath = "../fragShader.glsl";

